#include "libft.h"

int	ft_ispower(int n)
{
	int i;
	int ec;

	n = 1;
	i = 1;
	ec = 3;
	while (n < 100)
	{
		while (i < n)
		{
			i += ec;
			ec += 2;
		}
		if (i == n)
			ec = 1;
		n++;
		i = 1;
		ec = 3;
	}
	return (1);
}
