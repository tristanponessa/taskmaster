#include "../../includes/libft.h"

void	*ft_memcpy(void *dst, const void *src, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		((unsigned char*)dst)[i] = ((unsigned char*)src)[i];
		i++;
	}
	return (dst);
}
