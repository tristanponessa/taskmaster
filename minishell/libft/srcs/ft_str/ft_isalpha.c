#include "../../includes/libft.h"

int		ft_isalpha(int c)
{
	if ((65 <= c && c <= 90) || (97 <= c && c <= 122))
		return (1);
	return (0);
}

int		ft_iscap(int ch)
{
	if (ch >= 'A' && ch <= 'Z')
		return (1);
	return (0);
}
